CREATE TABLE provincias (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nombre VARCHAR NOT NULL,
  fecha_creacion TIMESTAMP DEFAULT NOW()
);

CREATE TABLE ciudades (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  provincia_id INT NOT NULL,
  nombre VARCHAR NOT NULL,
  fecha_creacion TIMESTAMP DEFAULT NOW()
);

CREATE TABLE barrios (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ciudad_id INT NOT NULL,
  nombre VARCHAR NOT NULL,
  fecha_creacion TIMESTAMP DEFAULT NOW()
);

CREATE TABLE usuarios (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  barrio_id INT NOT NULL,
  nombre VARCHAR NOT NULL,
  usuario VARCHAR UNIQUE NOT NULL,
  contrasena_hash VARCHAR NOT NULL,
  rol VARCHAR DEFAULT 'Vecino',
  fecha_registro TIMESTAMP DEFAULT NOW()
);

CREATE TABLE reportes (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  usuario_id INT NOT NULL,
  barrio_id INT NOT NULL,
  direccion VARCHAR NOT NULL,
  ubicacion VARCHAR NOT NULL,
  foto_url VARCHAR NOT NULL,
  estado VARCHAR DEFAULT 'Pendiente',
  descripcion TEXT NOT NULL,
  activo BOOLEAN DEFAULT true,
  fecha_creacion TIMESTAMP DEFAULT NOW(),
  fecha_actualizacion TIMESTAMP DEFAULT NOW()
);

CREATE TABLE gestiones_directiva (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reporte_id INT NOT NULL,
  lider_id INT NOT NULL,
  estado_asignado VARCHAR NOT NULL,
  mensaje TEXT NOT NULL,
  fecha_gestion TIMESTAMP DEFAULT NOW()
);

CREATE TABLE comentarios (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  reporte_id INT NOT NULL,
  usuario_id INT NOT NULL,
  mensaje TEXT NOT NULL,
  activo BOOLEAN DEFAULT true,
  fecha_creacion TIMESTAMP DEFAULT NOW()
);

CREATE TABLE apoyos (
  id INT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  usuario_id INT NOT NULL,
  reporte_id INT NOT NULL,
  fecha TIMESTAMP DEFAULT NOW()
);

CREATE UNIQUE INDEX ON apoyos (usuario_id, reporte_id);

COMMENT ON COLUMN reportes.usuario_id IS 'Vecino que reporta la incidencia inicial';
