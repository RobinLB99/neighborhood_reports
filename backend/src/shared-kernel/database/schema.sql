COMMENT ON COLUMN reportes.ubicacion IS 'Coordenadas X/Y en el mapa';
COMMENT ON COLUMN reportes.estado IS 'Estado actual para consultas rápidas';
COMMENT ON COLUMN reportes.activo IS 'Borrado lógico';

COMMENT ON COLUMN gestiones_directiva.reporte_id IS 'El reporte que se está gestionando';
COMMENT ON COLUMN gestiones_directiva.lider_id IS 'El líder específico que emite esta respuesta';
COMMENT ON COLUMN gestiones_directiva.estado_asignado IS 'El estado que se definió en este paso (ej. En_proceso)';
COMMENT ON COLUMN gestiones_directiva.mensaje IS 'La respuesta o actualización oficial';

